# 상위 5개 경로를 선택한 승객들의 이름 추출하기
# 탑승객 수로 순위 매기기
SELECT *,
       ROW_NUMBER() OVER(ORDER BY N_PASSENGERS DESC) RNK
  FROM (SELECT BOARDED,
               DESTINATION,
               COUNT(PASSENGERID) N_PASSENGERS
          FROM MYDATA.DATASET4
         GROUP BY BOARDED,
                  DESTINATION) BASE;
				
# 상위 5개 경로 선택 후 해당 경로를 테이블로 생성하기
CREATE TEMPORARY TABLE MYDATA.ROUTE AS
SELECT BOARDED,
       DESTINATION
  FROM (SELECT *,
	       ROW_NUMBER() OVER(ORDER BY N_PASSENGERS DESC) RNK
	  FROM (SELECT BOARDED,
                       DESTINATION,
                       COUNT(PASSENGERID) N_PASSENGERS
		  FROM MYDATA.DATASET4
		 GROUP BY BOARDED, DESTINATION) BASE
	) BASE
 WHERE RNK BETWEEN 1 AND 5;
 
# 생성한 경로에 해당하는 승객들의 이름 추출하기
SELECT NAME_WIKI,
       A.BOARDED,
       A.DESTINATION
  FROM MYDATA.DATASET4 A
 INNER
  JOIN MYDATA.ROUTE B
    ON A.BOARDED = B.BOARDED AND A.DESTINATION = B.DESTINATION;
    
