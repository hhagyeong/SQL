# 연령에 성별을 추가해서 생존율 파악하기
SELECT FLOOR(AGE/10)*10 AGEBAND,
       SEX,
       COUNT(PASSENGERID) N_PASSENGERS,
       SUM(SURVIVED) N_SURVIVED,
